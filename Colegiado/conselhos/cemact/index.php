                Art. 8º – São atribuições do Presidente: <br>
                I – Convocar e presidir as reuniões ordinárias e extraordinárias do Conselho; <br>
                II – Aprovar as pautas das reuniões; <br>
                III – Assinar em conjunto com o Secretário os documentos de competência do CEMAT; <br>
                IV – Baixar atos decorrentes das decisões do conselho; <br>
                V – Determinar a realização de estudos solicitados pelo Conselho; <br>
                VI – Criar comissões especiais, cujos membros serão indicados pelo plenário; e, <br>
                VII – Desempenhar outras atribuições inerantes ao cargo. <br>
                SUBSEÇÃO II <br>
                DO PLENÁRIO <br>
                Art. 9º – Compõem o plenário do Conselho os Membros previstos no Artigo 4º deste
                Regimento. <br>
                Art. 10 – São atribuições dos Membros do Conselho: <br>
                I – Estabelecer a politítica e a estratégia global do Meio Ambiente, Ciência e Tecnologia; <br>
                II – Devinir as áreas prioritárias para a pesquisa no âmbito Estadual; <br>
                III – Defineir a política de capacitação de recursos humanos para a área de Meio Ambiente,
                Ciência e Tecnologia; <br>
                IV – Definir critérios de penalidades por inadimplência contratuais ou descumprimentos à
                obrigações firmadas em convênios; <br>
                V – Estabelecer critérios de seleção, acompanhamento e avaliação dos programas e projetos; <br>
                VI – Definir Critérios para diagnosticar a oferta e demanda de Ciência e Tecnologia no Estado; <br>
                VII – Deliberar sobre o Sistema Estadual do Meio Ambiente, Ciência e Tecnologia; <br>
                VIII – Credenciar profissionais liberais e instituições para a avaliação de projetos de pesquisa; e, <br>
                IX – Instituir grupos de apoio técnico que se fizerem necessários. <br>
